// Map a 3D grid index (x, y, z) into a 1D index into the output vertex array.
function xyz_to_cube_index(x, y, z) {
  return (
    x + y * (NUMBER_CUBE_X + 1) + z * (NUMBER_CUBE_X + 1) * (NUMBER_CUBE_Y + 1)
  );
}

