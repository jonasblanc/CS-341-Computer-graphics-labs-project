export function terrain_build_mesh(chunk_offset) {
  const cornersInObject = [];

  const halfEdgePoints = [];
  const halfEdgesNormals = [];
  const halfEdgesNormalsContribution = [];
  const faces = [];

  // Compute for each cube corner if it is in the object
  for (let gx = 0; gx <= NUMBER_CUBE_X; gx++) {
    for (let gy = 0; gy <= NUMBER_CUBE_Y; gy++) {
      for (let gz = 0; gz <= NUMBER_CUBE_Z; gz++) {
        const idx_corner = xyz_to_cube_index(gx, gy, gz);
        const noiseCoordinates = cubeCoordinateToNoiseCoordinate(
          gx,
          gy,
          gz,
          chunk_offset
        );
        cornersInObject[idx_corner] = noise3D(noiseCoordinates);
      }
    }
  }

  // Compute for each cube its hafl edge points
  // It adds border cubes outside the area in order to cover all half edge points
  for (let gx = 0; gx <= NUMBER_CUBE_X; gx++) {
    for (let gy = 0; gy <= NUMBER_CUBE_Y; gy++) {
      for (let gz = 0; gz <= NUMBER_CUBE_Z; gz++) {
        const cubeIdx = xyz_to_cube_index(gx, gy, gz);
        const idx = 3 * cubeIdx;

        const shiftVect = [
          [0.5, 0.0, 0.0],
          [0.0, 0.5, 0.0],
          [0.0, 0.0, 0.5],
        ];
        for (let i = 0; i < 3; i++) {
          const halfEdgeShift = shiftVect[i];
          const drawingCoordinates = cubeCoordinatesToDrawingCoordinates(
            [gx, gy, gz],
            chunk_offset,
            halfEdgeShift
          );
          halfEdgePoints[idx + i] = drawingCoordinates;
          halfEdgesNormals[idx + i] = [0, 0, 0];
          halfEdgesNormalsContribution[idx + i] = [0];
        }
      }
    }
  }

  for (let gx = 0; gx < NUMBER_CUBE_X; gx++) {
    for (let gy = 0; gy < NUMBER_CUBE_Y; gy++) {
      for (let gz = 0; gz < NUMBER_CUBE_Z; gz++) {
        /*
         * We create a cube if the visibility is above 0 and nothing if it's below
         */
        // we put the value between 0...1 so that it could be stored in a non-float texture on older browsers/GLES3, the -0.5 brings it back to -0.5 ... 0.5

        const areCornersInObject = getAreCubeCornersInObject(
          gx,
          gy,
          gz,
          cornersInObject
        );

        if (isOnSurface(areCornersInObject)) {
          const halfEdgePointsIndexes = getIndexHalfEdgePoints(gx, gy, gz);
          const LUTEntry =
            LOOK_UP_TABLE[get_binary_idx_from_corners(areCornersInObject)];

          //const LUTEntry = test_face;
          const trianglesIndexInCube =
            translateLUTEntryToIndexOfHalfEdgePointInTheCube(LUTEntry);

          for (
            let face_idx = 0;
            face_idx < trianglesIndexInCube.length;
            ++face_idx
          ) {
            const current_face = [];
            const triangle = trianglesIndexInCube[face_idx];

            for (
              let vertex_idx = 0;
              vertex_idx < triangle.length;
              ++vertex_idx
            ) {
              const hafleEdgeNumeE = triangle[vertex_idx];
              const p_idx = halfEdgePointsIndexes[hafleEdgeNumeE];
              current_face.push(p_idx);

              const shift = [
                [0, 0, 0],
                [0, 1, 0],
                [1, 1, 0],
                [1, 0, 0],
                [0, 0, 1],
                [0, 1, 1],
                [1, 1, 1],
                [1, 0, 1],
              ];

              const cornerNums = getAdjacentCornersNum(hafleEdgeNumeE);
              // Corner pos
              const P1 = cubeCoordinatesToDrawingCoordinates(
                vec3.add([0, 0, 0], [gx, gy, gz], shift[cornerNums[0]]),
                chunk_offset,
                [0, 0, 0]
              );
              const P2 = cubeCoordinatesToDrawingCoordinates(
                vec3.add([0, 0, 0], [gx, gy, gz], shift[cornerNums[1]]),
                chunk_offset,
                [0, 0, 0]
              );
              // Corner noise value
              const NV1 = areCornersInObject[cornerNums[0]];
              const NV2 = areCornersInObject[cornerNums[1]];
              const halfedgePos = vec3.add(
                [0, 0, 0],
                P1,
                vec3.scale(
                  [0, 0, 0],
                  vec3.sub([0, 0, 0], P2, P1),
                  (ISO_VALUE - NV1) / (NV2 - NV1)
                )
              );
              // console.log(halfEdgePoints[p_idx], halfedgePos);
              halfEdgePoints[p_idx] = halfedgePos;
            }
            faces.push(current_face);

            // Normal computation
            const p1 = halfEdgePoints[current_face[0]];
            const p2 = halfEdgePoints[current_face[1]];
            const p3 = halfEdgePoints[current_face[2]];

            const p1p2 = vec3.sub([0, 0, 0], p2, p1);
            const p1p3 = vec3.sub([0, 0, 0], p3, p1);
            const face_normal = vec3.normalize(
              [0, 0, 0],
              vec3.cross([0, 0, 0], p1p3, p1p2)
            );

            // Update normal of the three trangle vertex
            for (let j = 0; j < current_face.length; ++j) {
              const p_idx = current_face[j];
              const wheighted_previous_normal = vec3.scale(
                [0, 0, 0],
                halfEdgesNormals[p_idx],
                halfEdgesNormalsContribution[p_idx]
              );
              halfEdgesNormals[p_idx] = vec3.normalize(
                [0, 0, 0],
                vec3.add([0, 0, 0], wheighted_previous_normal, face_normal)
              );
              halfEdgesNormalsContribution[p_idx] += 1;
            }
          }
        }
      }
    }
  }

  return {
    vertex_positions: halfEdgePoints,
    vertex_normals: halfEdgesNormals,
    faces: faces,
  };
}

/**
 *  Compute for the current cube corners if they are in the object
 *  Order based on drawing below
 */
function getAreCubeCornersInObject(gx, gy, gz, cornersInObject) {
  const v0_corner = xyz_to_cube_index(gx, gy, gz);
  const v1_corner = xyz_to_cube_index(gx, gy + 1, gz);
  const v2_corner = xyz_to_cube_index(gx + 1, gy + 1, gz);
  const v3_corner = xyz_to_cube_index(gx + 1, gy, gz);
  const v4_corner = xyz_to_cube_index(gx, gy, gz + 1);
  const v5_corner = xyz_to_cube_index(gx, gy + 1, gz + 1);
  const v6_corner = xyz_to_cube_index(gx + 1, gy + 1, gz + 1);
  const v7_corner = xyz_to_cube_index(gx + 1, gy, gz + 1);

  const currentCubeCornersInObject = [
    cornersInObject[v0_corner],
    cornersInObject[v1_corner],
    cornersInObject[v2_corner],
    cornersInObject[v3_corner],
    cornersInObject[v4_corner],
    cornersInObject[v5_corner],
    cornersInObject[v6_corner],
    cornersInObject[v7_corner],
  ];

  return currentCubeCornersInObject;
}

