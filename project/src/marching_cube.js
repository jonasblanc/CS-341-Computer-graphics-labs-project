    (chunk_offset[1] + (xyz[1] + halfEdgeShift[1]) / NUMBER_CUBE_Y) *
      CHUNK_SIZE_Y -
    0.5;
  const mapped_Z =
    (chunk_offset[2] + (xyz[2] + halfEdgeShift[2]) / NUMBER_CUBE_Z) *
      CHUNK_SIZE_Z -
    0.5;
  return [mapped_X, mapped_Y, mapped_Z];
